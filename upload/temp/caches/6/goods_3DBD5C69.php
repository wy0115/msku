<div class="para-title level-2" label-module="para-title" style="clear: both; zoom: 1; overflow: hidden; border-left: 12px solid rgb(79, 156, 238); line-height: 24px; font-size: 22px; font-family: &quot;Microsoft YaHei&quot;, SimHei, Verdana; margin: 35px 0px 15px -30px; background-image: url(&quot;https://bkssl.bdimg.com/static/wiki-lemma/normal/resource/img/paraTitle-line_c5e6d61.png&quot;); background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial; position: relative; color: rgb(51, 51, 51);">
<h2 class="title-text" style="margin: 0px; padding: 0px 8px 0px 18px; font-size: 22px; color: rgb(0, 0, 0); float: left; line-height: 24px; font-weight: 400; background-image: initial; background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial;">营养成份</h2>
<a class="edit-icon j-edit-link" data-edit-dl="7" style="color: rgb(136, 136, 136); display: block; float: right; height: 22px; line-height: 22px; padding-left: 24px; font-size: 12px; font-family: SimSun; background-image: initial; background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial;">编辑</a></div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">饮茶有许多益处，这是众所周知的。但饮茶为什么会有许多好处呢？这对一般人来说，是<a target="_blank" href="https://baike.baidu.com/item/%E7%9F%A5%E5%85%B6%E7%84%B6%E8%80%8C%E4%B8%8D%E7%9F%A5%E5%85%B6%E6%89%80%E4%BB%A5%E7%84%B6" style="color: rgb(19, 110, 194); text-decoration-line: none;">知其然而不知其所以然</a>。随着科学的发展，到了19世纪初，茶业的成分才逐渐明确起来。经过现代科学的分离和鉴定，乌龙茶中含有机化学成分达四百五十多种，无机矿物元素达四十多种。茶叶中的有机化学成分和无机矿物元素含有许多营养成分和药效成分。有机化学成分主要有：茶多酚类、植物碱、蛋白质、氨基酸、维生素、果胶素、有机酸、<a target="_blank" href="https://baike.baidu.com/item/%E8%84%82%E5%A4%9A%E7%B3%96" style="color: rgb(19, 110, 194); text-decoration-line: none;">脂多糖</a>、糖类、酶类、色素等。而铁观音所含的有机化学成分，如茶多酚、<a target="_blank" href="https://baike.baidu.com/item/%E5%84%BF%E8%8C%B6%E7%B4%A0" style="color: rgb(19, 110, 194); text-decoration-line: none;">儿茶素</a>、多种氨基酸等含量，明显高于其他茶类。无机矿物元素主要有：钾、钙、镁、钴、铁、锰、铝、钠、锌、铜、氮、磷、氟、碘、硒等。铁观音所含的无机矿物元素，如锰、铁、氟、钾、钠等均高于其他茶类。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;"><a target="_blank" href="https://baike.baidu.com/item/%E4%B9%8C%E9%BE%99%E5%87%8F%E8%82%A5%E8%8C%B6" style="color: rgb(19, 110, 194); text-decoration-line: none;">乌龙减肥茶</a>利用福建特产乌龙茶及纯中药精心配制和先进的工艺制作而成,国家保健食品批文。品质有保障。是健康减肥的佳品。</div>
<div class="anchor-list" style="position: relative; color: rgb(51, 51, 51); font-family: arial, 宋体, sans-serif;"><a name="8" class="lemma-anchor para-title" style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="sub123473_8" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="冲泡方法" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a></div>
<div class="para-title level-2" label-module="para-title" style="clear: both; zoom: 1; overflow: hidden; border-left: 12px solid rgb(79, 156, 238); line-height: 24px; font-size: 22px; font-family: &quot;Microsoft YaHei&quot;, SimHei, Verdana; margin: 35px 0px 15px -30px; background-image: url(&quot;https://bkssl.bdimg.com/static/wiki-lemma/normal/resource/img/paraTitle-line_c5e6d61.png&quot;); background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial; position: relative; color: rgb(51, 51, 51);">
<h2 class="title-text" style="margin: 0px; padding: 0px 8px 0px 18px; font-size: 22px; color: rgb(0, 0, 0); float: left; line-height: 24px; font-weight: 400; background-image: initial; background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial;">冲泡方法</h2>
<a class="edit-icon j-edit-link" data-edit-dl="8" style="color: rgb(136, 136, 136); display: block; float: right; height: 22px; line-height: 22px; padding-left: 24px; font-size: 12px; font-family: SimSun; background-image: initial; background-position: initial; background-size: initial; background-repeat: initial; background-attachment: initial; background-origin: initial; background-clip: initial;">编辑</a></div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">盖碗泡茶法</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;"><a target="_blank" href="https://baike.baidu.com/item/%E8%8C%B6%E5%85%B7" style="color: rgb(19, 110, 194); text-decoration-line: none;">茶具</a>为茶盘、茶巾、煮水器、盖杯、茶杯、茶船、渣匙、茶盅等。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">① 看干茶：先审察干茶的形状与色泽，以了解茶叶品质特性。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">② 烫茶具：以沸水冲泡洗茶碗、茶具以符卫生。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">③ 放置茶叶：参酌茶叶特性，放置<a target="_blank" href="https://baike.baidu.com/item/%E8%8C%B6%E5%A3%B6" style="color: rgb(19, 110, 194); text-decoration-line: none;">茶壶</a>容量1/3的茶叶。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">④ 冲泡：以沸水冲入壶中，冲满即刻盖好，冲泡开水温度以95-100℃为宜，可以多次冲泡以供长时间品饮。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑤ 冲茶碗：将热水冲入杯内，用杯盖轻拨泡沫，将杯盖上泡沫冲掉盖上杯盖。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑥ 冲泡时间：冲泡时间由短而长，第一次短而后逐次增长。泡茶之时间长短不同，茶汤中可溶物的量与质亦是不同，因此冲泡茶的时间长短直接影响茶汤品质。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑦ 温杯：将茶杯先用沸水冲净烫温，以助长香味兼重卫生。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑧ 倾倒茶汤：将冲泡出来之茶汤，先倾入茶海，使其浓度匀一，茶末沉淀，然后均匀注入茶杯供饮。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑨ 品茶：冻顶乌龙茶具有明显的花香近似桂花香，特有香气清纯持久，芳香甘醇，生津解渴，提神醒脑，滋味特别甘醇。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">盖碗茶之好处：a.碗口较大，便于冲泡茶叶。碗底圆小，注入开水茶叶硕碗底翻滚，易泡出茶味。b.碗盖使香气凝集，揭开碗盖，茶香四溢并用盖赶浮叶，不使沾唇，便于品饮。c.茶船可免端茶烫手，若碗里溢出茶水，流入茶船，便函于端茶敬客。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">宜兴茶壶泡茶法</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">茶具为茶盘、茶巾、煮水器、茶壶（宜兴县出产陶制小茶壶）、茶杯、茶船、渣匙、茶盅、闻香杯等。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">① 烫茶具：以沸水冲泡茶壶，茶具以待卫生。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">② 放置茶叶：用茶荷将茶叶取出适量放入壶中。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">③ 冲泡：冲入开水，并使泡沫溢出，随即加盖，并将茶汤倒入茶船之中；再次冲入开水。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">④ 冲壶：即刻从壶盖上冲浇开水使茶壶里外保温，藉以充分浸出滋味发挥香气。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑤ 冲泡时间：冲泡时间由短而长，第一次短而后逐次增长。泡茶之时间长短不同，茶汤中可溶物的量与质亦是不同，因此冲泡茶的时间长短直接影响茶汤品质。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">⑥ 倾倒茶汤：将茶倒入茶盅，然后倒入闻香杯，随即将闻香杯置于鼻前吸气数次，闻其香味。其目的为了品尝&quot;杯底留香&quot;，&quot;温香&quot;、&quot;次香&quot;，愈是好茶，留香愈久，香气愈富变化。所以，也只有上等好茶才经得起这个考验。</div>
