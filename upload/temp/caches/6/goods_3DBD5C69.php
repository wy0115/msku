<div class="para-title level-3" label-module="para-title" style="clear: both; zoom: 1; margin: 20px 0px 12px; line-height: 20px; font-size: 18px; font-family: &quot;Microsoft YaHei&quot;, SimHei, Verdana; color: rgb(51, 51, 51);">
<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">降血脂</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">1981年，日本和洋女子大学宫川丰美和川村一男教授，就乌龙茶对动物脂肪代谢的影响进行试验，证明能显著抑制血中胆固醇及中性脂肪的增加。1983年，福建省中医药研究所观察了一组血中胆固醇较高的病人，在停用各种降脂药物的情况下，每日上下午两次饮用乌龙茶，连续24<a target="_blank" href="https://baike.baidu.com/item/%E5%91%A8%E5%90%8E" style="color: rgb(19, 110, 194); text-decoration-line: none;">周后</a>，病人血中胆固醇含量有不同程度下降。进一步的动物试验表明，乌龙茶有防止和减轻血中脂质在主动脉粥样硬化作用。饮用乌龙茶还可以降低血液粘稠度，防止红细胞集聚，改善血液高凝状态，增加血液流动性，改善微循环。这对于防止血管病变，血管内血栓形成以及中医?quot;瘀血症\&quot;均有积极意义。此外，体外血栓形成试验，也表明乌龙茶有抑制血栓形成的作用。</div>
<div class="anchor-list" style="position: relative; color: rgb(51, 51, 51); font-family: arial, 宋体, sans-serif;"><a name="6_3" class="lemma-anchor para-title" style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="sub123473_6_3" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="抗衰老" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="6-3" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a></div>
<div class="para-title level-3" label-module="para-title" style="clear: both; zoom: 1; margin: 20px 0px 12px; line-height: 20px; font-size: 18px; font-family: &quot;Microsoft YaHei&quot;, SimHei, Verdana; color: rgb(51, 51, 51);">
<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">抗衰老</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">1983年，福建省中医药研究所进行抗衰老试验表明，他们分别加喂乌龙茶和维生素E的两组动物，肝脏内脂质过氧化均明显减少，这说明乌龙茶和维生素E一样有抗衰老功效。人体试验还表明，在每日内服足量维生素C情况下，饮用乌龙茶可以使血中维生素C含量持较高水平，尿中维生素C排出量减少，而维生素C的抗衰老作用早已被研究证明。因此，饮用乌龙茶可以从多方面增强人体抗衰老能力。1985年10月<a target="_blank" href="https://baike.baidu.com/item/%E7%8E%8B%E6%B3%BD%E5%86%9C" style="color: rgb(19, 110, 194); text-decoration-line: none;">王泽农</a>教授访问安溪茶乡时，赋诗一首，对<a target="_blank" href="https://baike.baidu.com/item/%E5%AE%89%E6%BA%AA%E4%B9%8C%E9%BE%99%E8%8C%B6" style="color: rgb(19, 110, 194); text-decoration-line: none;">安溪乌龙茶</a>的保健功效作了高度的概括：安溪芳若铁观音，益寿延年六根清。新选名茶黄金桂，堪称妙药保丹心。久服千朝姿容美，能疗百病体态轻。</div>
<div class="anchor-list" style="position: relative; color: rgb(51, 51, 51); font-family: arial, 宋体, sans-serif;"><a name="6_4" class="lemma-anchor para-title" style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="sub123473_6_4" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="其他功效" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a><a name="6-4" class="lemma-anchor " style="color: rgb(19, 110, 194); position: absolute; top: -50px;"></a></div>
<div class="para-title level-3" label-module="para-title" style="clear: both; zoom: 1; margin: 20px 0px 12px; line-height: 20px; font-size: 18px; font-family: &quot;Microsoft YaHei&quot;, SimHei, Verdana; color: rgb(51, 51, 51);">
<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">其他功效</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">台湾乌龙茶具有瘦身的功效与作用。台湾乌龙茶之所以流行，完全是因为它溶解脂肪的减肥效果，这种说法也确实有科学的根据。饮用乌龙茶可以提升类蛋白脂肪酶的功能，也就是说，并非乌龙茶本身能溶解脂肪，而是它可以提高分解脂肪的酵素，所以饮用乌龙茶后，脂肪代谢量也相对地提高了，从而起到了减肥瘦身的功效。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">台湾乌龙茶具有预防蛀牙的功效与作用。台湾乌龙茶中含有的多酚类具有能够抑制齿垢酵素产生的功效，所以吃饭之后饮用一杯乌龙茶，可以防止齿垢和蛀牙的发生。蛀牙形成的原因是由于细菌侵入牙齿组织，而且在组织内产生引起蛀牙的酵素，这种酵素和食物中所含有的糖分起作用，产生蛀蚀牙齿的物质。这种可以蛀蚀牙齿的物质与细菌附着在牙齿上即形成齿垢，累积之后就会发生蛀牙现象。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">台湾乌龙茶具有抗肿瘤、预防老化的功效与作用。台湾乌龙茶具有促进分解血液中脂肪的功效，也能降低胆固醇的含量。此外，乌龙茶也有抗肿瘤、提高淋巴细胞及NK细胞的活化作用，以及加强免疫功能、预防老化等作用。专家已经发现乌龙茶多酚类还有吸着体内异物并使其一起排出体外的功效。</div>
