<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">1998年6月15日《健康报》(记者郑远翔)报道：不久前，由<a target="_blank" href="https://baike.baidu.com/item/%E4%B8%AD%E5%9B%BD%E9%A2%84%E9%98%B2%E5%8C%BB%E5%AD%A6%E7%A7%91%E5%AD%A6%E9%99%A2" style="color: rgb(19, 110, 194); text-decoration-line: none;">中国预防医学科学院</a>营养与食品卫生研究所毒理和化学研究室副研究员<a target="_blank" href="https://baike.baidu.com/item/%E9%9F%A9%E9%A9%B0" style="color: rgb(19, 110, 194); text-decoration-line: none;">韩驰</a>和她的助手<a target="_blank" href="https://baike.baidu.com/item/%E5%BE%90%E5%8B%87" style="color: rgb(19, 110, 194); text-decoration-line: none;">徐勇</a>，进行茶叶在动物体内的抑癌试验。他们分别给大白鼠喂<a target="_blank" href="https://baike.baidu.com/item/%E5%AE%89%E6%BA%AA%E9%93%81%E8%A7%82%E9%9F%B3" style="color: rgb(19, 110, 194); text-decoration-line: none;">安溪铁观音</a>等五种茶，同时给予喂人工合成的纯度大于99.8%的致癌物甲基卡基亚硝胶。三个月后，大白鼠食道癌发生率为42-67%，患癌鼠平均瘤数为2.2-3个。而未饮茶的大白鼠食道癌发病率为90%，患癌鼠平均瘤数为5.2个，五种茶叶抑癌效果为安溪铁观音最佳。与此同时，他们还进行另一种试验，即用亚硝酸纳和甲基卡胶做致癌前体物，结果发现，饮茶组的大白鼠无一发生食道癌，未饮茶组发生率为100%。这一结果证明，茶叶可全部阻断亚硝胶的体内内源性的形成。</div>
