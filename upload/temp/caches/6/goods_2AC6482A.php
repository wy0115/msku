<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">窨花</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">用玫瑰花进行窨制，以提高茶叶香气。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（1）茶坯与鲜花处理；窨制前茶坯需进行复火，使含水量降到5%左右。复火后当茶坯温度降到40℃左右时进行窨花。玫瑰鲜花经过适当摊放，摘去花蒂、<a target="_blank" href="https://baike.baidu.com/item/%E8%8A%B1%E8%95%8A/7380120" data-lemmaid="7380120" style="color: rgb(19, 110, 194); text-decoration-line: none;">花蕊</a>，经筛花后，以净花瓣付窨。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（2）茶花拌和；窨制时，按一层茶叶一层花的方式堆放，配花量25%，10小时后通花一次，通花后续窨8小时，然后起花复火。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（3）起花复火；将花渣从茶叶中分离出来，茶坯复火，复火后茶叶含水量为6.5%～7.0%。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（4）提花；提花的作用是提高茶叶香气的鲜灵度。提花所用的鲜花质量要好，用量不宜过多，以6%为宜。提花后将茶中花渣捡出，窨制好的玫瑰茶用铝箔复合袋真空包装，低温贮藏。<span style="font-size: 12px; line-height: 0; position: relative; vertical-align: baseline; top: -0.5em; margin-left: 2px; color: rgb(51, 102, 204); cursor: default; padding: 0px 2px;">[4]</span><a class="sup-anchor" name="ref_[4]_4793" style="color: rgb(19, 110, 194); position: relative; top: -50px; font-size: 0px; line-height: 0;">&nbsp;</a><a name="ref_4" style="color: rgb(19, 110, 194);"></a></div>
