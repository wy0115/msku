<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">玫瑰采摘</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">玫瑰花茶使用的玫瑰：落叶灌木，茎密生锐刺，羽状复叶，小叶5-9片，椭圆形成倒卵圆形，上面有皱纹，夏季开花，花单生，紫生色至白色，有浓郁芳香。玫瑰花采下后，经适当摊放，折瓣，拣去花蒂、花蕊，以净花瓣付窨。广东玫瑰红茶实行单窨，下花量为100斤茶用10~16公斤花；福建玫瑰绿茶两窨一提，总下花量为100公斤茶用50公斤花；九曲红梅一窨一提，用花量为20公斤。</div>
