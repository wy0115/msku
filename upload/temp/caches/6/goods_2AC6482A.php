<h3 class="title-text" style="margin: 0px; padding: 0px; font-size: 18px; font-weight: 400;">茶坯制作</h3>
</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（1）<a target="_blank" href="https://baike.baidu.com/item/%E7%BB%BF%E8%8C%B6/13497" data-lemmaid="13497" style="color: rgb(19, 110, 194); text-decoration-line: none;">绿茶</a>茶坯的制作。鲜叶用小型滚筒杀青机杀青，杀青温度不可太高，一般控制在离杀青机筒壁4厘米处的空气温度为140℃左右。杀青后期注意降温，避免焦叶。杀青至叶色深且叶片萎软，香气显露时下机摊凉。揉捻方法同普通绿茶。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（2）红茶<a target="_blank" href="https://baike.baidu.com/item/%E8%8C%B6%E5%9D%AF" style="color: rgb(19, 110, 194); text-decoration-line: none;">茶坯</a>的制作。鲜叶先进行萎凋，萎凋时温度不可太高，环境温度30℃左右；时间不可太长，一般1.5~2小时。萎凋适度的特征是：叶色暗绿、香气显露，含水量60%左右。揉捻、发酵方法同<a target="_blank" href="https://baike.baidu.com/item/%E5%B7%A5%E5%A4%AB%E7%BA%A2%E8%8C%B6" style="color: rgb(19, 110, 194); text-decoration-line: none;">工夫红茶</a>。</div>
<div class="para" label-module="para" style="font-size: 14px; word-wrap: break-word; color: rgb(51, 51, 51); margin-bottom: 15px; text-indent: 2em; line-height: 24px; zoom: 1; font-family: arial, 宋体, sans-serif;">（3）黄茶茶坯的制作。黄茶的杀青工艺同普通绿茶制法。杀青叶出锅后趁热用双层牛皮纸包裹，放在箱或罐中进行闷黄，时间6~7小时，中间翻拌一次，使茶叶氧化程度一致，黄变均匀。</div>
